def _run_final(*, best_slot_code, executor, evaluator, project_root):
    sub_dir = project_root / "submission"
    sub_dir.mkdir(parents=True, exist_ok=True)
    logs_dir = project_root / "logs"
    eval_dir = logs_dir / "eval_artifacts"
    model_path = str(sub_dir / "model.keras")

    print("\n" + "=" * 60)
    print("  FINAL RUN: Retraining best model on ALL data")
    print("=" * 60)

    script = assemble_script(best_slot_code, is_final=True, model_save_path=model_path)
    script = _append_eval_wrapper(script, 999, eval_dir)
    script_path = logs_dir / "final_run.py"
    script_path.write_text(script, encoding="utf-8")

    t0 = time.time()
    result = executor.run_file(script_path)
    dt = time.time() - t0

    if result.success:
        print(f"  SUCCESS ({dt:.1f}s)")
        yt = eval_dir / "y_true_iter_999.npy"
        yp = eval_dir / "y_pred_iter_999.npy"
        if yt.exists() and yp.exists():
            ev = evaluator.evaluate_from_files(yt, yp)
            if ev.metrics.get("status") == "success":
                print(f"  macro_roc_auc = {ev.metrics['macro_roc_auc']:.6f}")
                print(f"  scored_species = {ev.metrics['num_scored_columns']}")
                print(f"  samples = {ev.metrics['num_samples']}")

        if Path(model_path).exists():
            mb = Path(model_path).stat().st_size / (1024*1024)
            print(f"  Model: {model_path} ({mb:.1f} MB)")

        nb_path = sub_dir / "kaggle_inference.ipynb"
        _generate_kaggle_notebook(best_slot_code, nb_path)
        print(f"  Kaggle notebook: {nb_path}")

        (sub_dir / "best_model_code.py").write_text(best_slot_code, encoding="utf-8")

        print(f"\n  submission/ contents:")
        for f in sorted(sub_dir.iterdir()):
            print(f"    {f.name} ({f.stat().st_size/1024:.1f} KB)")
    else:
        print(f"  FAILED ({dt:.1f}s)")
        print(f"  {_truncate(result.stderr, 2000)}")


