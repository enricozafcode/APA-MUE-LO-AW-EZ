    max_iters = config.get("max_iterations", 10)
    temp = config["llm"].get("temperature", 0.2)
    current_slot = DEFAULT_SLOT_CODE
    best_slot = DEFAULT_SLOT_CODE
    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": SEED_PROMPT},
    ]
    metrics_history = []
    best_auc = 0.0
    consec_fail = 0
    max_fail = config.get("max_failures_before_stop", 5)

    print("=" * 60)
    print("  BirdCLEF Autonomous Research Agent")
    print("=" * 60)
    print(f"  Model:      {config['llm']['model']}")
    print(f"  Iterations: {max_iters}")
    print(f"  Timeout:    {config['execution']['timeout_seconds']}s")
    print("=" * 60)

    for it in range(1, max_iters + 1):
        print(f"\n{'─'*50}\n  ITERATION {it}/{max_iters}\n{'─'*50}")

        # 1. Query LLM
        t0 = time.time()
        print("Querying LLM...")
        resp = llm.generate_from_messages(messages=messages, temperature=temp)
        print(f"LLM responded in {time.time()-t0:.1f}s")

        # 2. Extract & validate
        slot = extract_python_code(resp)
        if not slot:
            print("ERROR: No code in response")
            messages = _trim_and_append(messages, "assistant", resp)
            messages = _trim_and_append(messages, "user",
                "No ```python``` block found. Return ONLY code with get_training_config() and build_model().")
            consec_fail += 1
            metrics_history.append({"iteration": it, "status": "no_code"})
            if consec_fail >= max_fail: break
            continue

        issues = validate_slot_code(slot)
        if issues:
            print(f"Validation: {issues}")
            messages = _trim_and_append(messages, "assistant", resp)
            messages = _trim_and_append(messages, "user",
                f"Errors: {'; '.join(issues)}. Fix and return corrected code.")
            consec_fail += 1
            metrics_history.append({"iteration": it, "status": "validation_failed"})
            if consec_fail >= max_fail: break
            continue

        current_slot = slot
        exp_meta = extract_experiment_meta(slot)

        # 3. Assemble & execute
        full = assemble_script(slot)
        full = _append_eval_wrapper(full, it, eval_dir)
        sp = logs_dir / f"iteration_{it}.py"
        sp.write_text(full, encoding="utf-8")
        (logs_dir / f"slots_{it}.py").write_text(slot, encoding="utf-8")

        print(f"Executing {sp.name}...")
        if exp_meta:
            print(f"  [{exp_meta.get('model_type', '?')}] {exp_meta.get('architecture', '')} — {exp_meta.get('change', '')}")
        t0 = time.time()
        result = executor.run_file(sp)
        dt = time.time() - t0
        print(f"{'SUCCESS' if result.success else 'FAILED'} ({dt:.1f}s)")

        # 4. Evaluate
        em = None
        if result.success:
            consec_fail = 0
            yt = eval_dir / f"y_true_iter_{it}.npy"
            yp = eval_dir / f"y_pred_iter_{it}.npy"
            if yt.exists() and yp.exists():
                try:
                    ev = evaluator.evaluate_from_files(yt, yp)
                    em = ev.metrics
                except Exception as eval_err:
                    print(f"  Evaluation error (skipping): {eval_err}")
                    em = None
                if em.get("status") == "success":
                    auc = em["macro_roc_auc"]
                    print(f"  macro_roc_auc = {auc:.6f} | scored = {em['num_scored_columns']}")
                    if auc > best_auc:
                        best_auc = auc
                        best_slot = slot
                        print(f"  ★ NEW BEST ({best_auc:.6f})")
                        (logs_dir / "best_slots.py").write_text(slot, encoding="utf-8")
            metrics_history.append({
                "iteration": it,
                "status": "success",
                "time": round(dt, 1),
                "macro_roc_auc": em.get("macro_roc_auc") if em else None,
                "experiment_meta": exp_meta,
            })
        else:
            consec_fail += 1
            print(f"  stderr: {_truncate(result.stderr, 200)}")
            metrics_history.append({
                "iteration": it,
                "status": "failed",
                "time": round(dt, 1),
                "experiment_meta": exp_meta,
            })
            if consec_fail >= max_fail: break

        # 5. Feedback — detect CNN stagnation over last 3 successful runs
        recent_aucs = [
            m["macro_roc_auc"] for m in metrics_history[-3:]
            if m["status"] == "success" and m.get("macro_roc_auc") is not None
            and m.get("experiment_meta", {}).get("model_type") == "cnn"
        ]
        cnn_stagnating = (
            len(recent_aucs) >= 3
            and (max(recent_aucs) - min(recent_aucs)) < 0.02
        )
        fb = _build_feedback_prompt(stdout=result.stdout, stderr=result.stderr,
                                     is_error=not result.success, metrics=em,
                                     current_code=current_slot, cnn_stagnating=cnn_stagnating)
        messages = _trim_and_append(messages, "assistant", resp)
        messages = _trim_and_append(messages, "user", fb)
        _save_metrics(metrics_history, logs_dir / "metrics_history.json")

    # ── Summary ──
    print(f"\n{'='*60}")
    ok = sum(1 for m in metrics_history if m["status"] == "success")
    print(f"  EXPLORATION DONE: {ok}/{len(metrics_history)} successful, best AUC = {best_auc:.6f}")
    print(f"{'='*60}")

    (logs_dir / "conversation_history.json").write_text(json.dumps(messages, indent=2), encoding="utf-8")

    # ── Final run on ALL data ──
    if best_auc > 0:
        _run_final(best_slot_code=best_slot, executor=executor, evaluator=evaluator, project_root=project_root)
    else:
        print("\nNo successful iterations — skipping final run.")


